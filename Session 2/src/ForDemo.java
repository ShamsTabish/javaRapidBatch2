
public class ForDemo {
	public static void main(String[] args) {

		System.out.println("Statrting the loop");
		for(
				int number=0;// Initialization  // Executed only once (at the beginning of the loop)
				number<5; // Condition
				number++// Expression
				){
			System.out.println(number);
		}
		
	
		System.out.println("---End of Program---");
		
	}
}
